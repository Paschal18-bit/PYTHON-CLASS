"""
This program demonstrates basic arithmetic operations in Python.
It creates two variables and performs:
1. Addition
2. Subtraction
3. Multiplication
4. Division

The result of each operation is displayed with an explanatory message.
"""

# Create two variables
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Perform addition
sum_result = num1 + num2
print(f"This is the sum of the two numbers: {sum_result}")

# Perform subtraction
sub_result = num1 - num2
print(f"This is the difference of the two numbers: {sub_result}")

# Perform multiplication
mul_result = num1 * num2
print(f"This is the product of the two numbers: {mul_result}")

# Perform division
div_result = num1 / num2
print(f"This is the quotient of the two numbers: {div_result}")
